    public class ServerStart implements Runnable {

        @Override
        public void run() {
            try {
                ServerSocket serverSocket = new ServerSocket(59090);
                while (true) {
                    Socket clientSocket = null;
                    clientSocket = serverSocket.accept();
                    textAreaLog.append("Request: " + clientSocket.getInetAddress() + " - ");

                    // create each thread
                    Thread listener = new Thread(new ClientHandler(clientSocket));
                    listener.start();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
                textAreaLog.append(ex.getMessage());
            }
        }
    }

    public class ClientHandler implements Runnable {

        Socket socket;
        Path path;

        public ClientHandler(Socket clientSocket) {
            try {
                socket = clientSocket;
            } catch (Exception ex) {
                textAreaLog.append(ex.getMessage());
            }
        }

        @Override
        public void run() {
            try {
                // get the input stream from the connected socket
                InputStream inputStream = socket.getInputStream();

                // create a DataInputStream so we can read data from it.
                DataInputStream dataInputStream = new DataInputStream(inputStream);

                String message = dataInputStream.readUTF();
                textAreaLog.append(message + "\n");

                dataInputStream.close();

            } catch (IOException ex) {
                textAreaLog.append(ex.getMessage());
            }
        }
    }


